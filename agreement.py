    def get_agreement_keyboard(self) -> InlineKeyboardMarkup:
        """Возвращает клавиатуру для принятия соглашения."""
        buttons = [
            [InlineKeyboardButton(text="✅ Принимаю условия", callback_data="accept_agreement")],
            [InlineKeyboardButton(text="❌ Отклоняю", callback_data="decline_agreement")]
        ]
        return InlineKeyboardMarkup(inline_keyboard=buttons)