from aiogram.types import InlineKeyboardMarkup, InlineKeyboardButton
from database import Database

class AgreementHandler:
    """Класс для работы с пользовательскими соглашениями."""
    
    def __init__(self):
        self.db = Database()
        # Добавляем поле в базу для отслеживания принятия соглашения
        self._init_agreement_table()
    
    def _init_agreement_table(self):
        """Добавляет таблицу для отслеживания принятых соглашений."""
        try:
            import sqlite3
            with sqlite3.connect(self.db.db_path) as conn:
                cursor = conn.cursor()
                
                cursor.execute('''
                    CREATE TABLE IF NOT EXISTS user_agreements (
                        user_id INTEGER PRIMARY KEY,
                        agreement_accepted BOOLEAN DEFAULT FALSE,
                        accepted_at TIMESTAMP,
                        FOREIGN KEY (user_id) REFERENCES users (user_id)
                    )
                ''')
                
                conn.commit()
        except Exception as e:
            print(f"Error creating agreement table: {e}")
    
    def has_accepted_agreement(self, user_id: int) -> bool:
        """Проверяет, принял ли пользователь соглашение."""
        try:
            import sqlite3
            with sqlite3.connect(self.db.db_path) as conn:
                cursor = conn.cursor()
                
                cursor.execute('''
                    SELECT agreement_accepted FROM user_agreements 
                    WHERE user_id = ?
                ''', (user_id,))
                
                result = cursor.fetchone()
                return result[0] if result else False
                
        except Exception as e:
            print(f"Error checking agreement for user {user_id}: {e}")
            return False
    
    def accept_agreement(self, user_id: int):
        """Отмечает, что пользователь принял соглашение."""
        try:
            import sqlite3
            with sqlite3.connect(self.db.db_path) as conn:
                cursor = conn.cursor()
                
                cursor.execute('''
                    INSERT OR REPLACE INTO user_agreements 
                    (user_id, agreement_accepted, accepted_at)
                    VALUES (?, TRUE, CURRENT_TIMESTAMP)
                ''', (user_id,))
                
                conn.commit()
                
        except Exception as e:
            print(f"Error accepting agreement for user {user_id}: {e}")
    
